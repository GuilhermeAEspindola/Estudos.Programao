#include <stdio.h>
///quando eu utilizo a biblioteca <stdio.h> quer dizer que utilizarei funções pré-programada!///
#include <stdlib.h>
/// biblioteca <stdlib.h> Serve para pausar o programa na hora que ele for executado///
int main(){
///quando utilizo a função main ela significa que é o corpo principal do meu algoritmo!///
///Tudo que estiver dentro das chaves {} no código será executado pela máquina.///
    printf("hello, world!!\n");
/// printf() e uma função de entrada e saída da biblioteca <stdio.h> que ira mostrar uma mensagem para o usuário.///
/// "" as aspas servem para colocar o texto que ira ser mostrado para o usuário.///
/// /n serve para ter quebra de linha///
    system("pause");
/// Função que serve para impedir do algoritmo se encerre automaticamente.///
 
}